package pathFinder;

/**
 * Mark
 *
 * Represents a point in a 2D surface
 *
 * @author Henrique Salvadori Coelho - henriquesc@gmail.com - hcoelho.com
 */

public class Mark {
  public int x;
  public int y;

  /**
   * Starts the mark
   *
   * @param x The X coordinate
   * @param y the Y coordinate
   */
  public Mark(int x, int y) {
    this.x = x;
    this.y = y;
  }
}
